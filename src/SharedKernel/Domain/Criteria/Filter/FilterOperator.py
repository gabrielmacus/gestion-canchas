from enum import Enum
from dataclasses import dataclass

class FilterOperators(Enum):
    EQ = "eq"
    NEQ = "neq"
    GT = "gt"
    LT = "lt"
    GTE = "gte"
    LTE = "lte"
    CONTAINS = "contains"
    NOT_CONTAINS = "not_contains"
    STARTS_WITH = "starts_with"
    ENDS_WITH = "ends_with"
    
@dataclass(frozen=True)
class FilterOperator:
    value: FilterOperators